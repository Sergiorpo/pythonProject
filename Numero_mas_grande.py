# Script que indica que numero es el mas grande.

uno = int(input("Dime tu primer numero: "))
dos = int(input("Dime tu segundo numero: "))
tres = int(input("Dime tu tercer numero: "))
maximo = max(uno,dos,tres)
minimo = min(uno,dos,tres)
print("El numero mas grande es {}".format(maximo))
print("El numero mas pequenio es {}".format(minimo))
